package com.nit.logical.string.methods;

public class CharAtDemo1 {

	public static void main(String[] args) {
		String str = new String("Hyderabad");
		char ch = str.charAt(3);
		System.out.println("Character at 3rd position is : "+ch);

		ch = str.charAt(8);
		System.out.println("Character at 8th position is : "+ch);
		
	
	}

}
